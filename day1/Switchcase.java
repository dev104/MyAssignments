package week1.day1;

public class Switchcase {
	public static void main (String args[]) {
		
		 char Name = 'D';
			
		  switch (Name) {
			
			case 'C':
				
				System.out.println("Chintu");
				break;
				
			case 'D':
				
				System.out.println("Dhisha");
				break;
				
			case 'M':
				
				System.out.println("Mottu");
				break;
				
			case 'K':
				
				System.out.println("Krish");
				break;
				
			case 'Y':
				
				System.out.println("Yaash");
				break;
			
			}
			
		}
}
