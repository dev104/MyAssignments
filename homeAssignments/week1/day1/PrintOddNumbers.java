package homeAssignments.week1.day1;

public class PrintOddNumbers {

		public static void main (String args[]) 
		{
			
			int s = 10;
			
			System.out.println("The Odd Numbers are: ");
			
			for (int i=0; i<=s; i++) {
				
				if(i%2 != 0) {
					System.out.println(i);
					
				}
			}
			
			
		}

	}


